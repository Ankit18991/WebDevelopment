var num1 = 45;
var num2 = 3;

// Arithmatic Operators
console.log("The values of num1 + num2 = " + (num1 + num2));
console.log("The Value of num1 - num2 = " + (num1-num2));
console.log("The Value of num1 * num2 = " + (num1*num2));
console.log("The Value of num1 / num2 = " + (num1/num2));
console.log("The Value of num1 ** num2 = " + (num1 ** num2));

// Unary Operators
console.log("The Value of num1++ = " + (num1++));
console.log("The Value of ++num1 = " + (++num1));

num1 = -num1;
console.log("The negative Value of num1 = " + num1);